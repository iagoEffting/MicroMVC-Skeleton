<?php

/*
|--------------------------------------------------------------------------
| Create The Application
|--------------------------------------------------------------------------
*/
$app = new \IagoEffting\MicroMVC\Application(
  realpath(__DIR__.'/../')
);

/*
|--------------------------------------------------------------------------
| Set Resquest
|--------------------------------------------------------------------------
*/
$app->setRequest(new \IagoEffting\MicroMVC\Http\Request($_SERVER['REQUEST_URI']));

/*
|--------------------------------------------------------------------------
| Set Path Configs
|--------------------------------------------------------------------------
*/
$app->setConfigs($app->basePath().'/config/');

/*
|--------------------------------------------------------------------------
| Set Providers Paths
|--------------------------------------------------------------------------
*/
$app->setProviders($app->basePath().'/app/Providers/');

/*
|--------------------------------------------------------------------------
| Return The Application
|--------------------------------------------------------------------------
*/
return $app;